""" Utils module *Fix* """

def str_to_upper(substr):
    """
    Transforms test to 'upper' style text
    :param substr: text to convert
    :type substr: string
    :return: converted text
    :rtype: string
    """
    return substr.upper()


def str_to_title(substr):
    """
    Transforms test to 'Title' style text
    :param substr: text to convert
    :type substr: string
    :return: converted text
    :rtype: string
    """
    return substr.title()

